f= input ("Enter Frist number:")
op=input("Enter operation (+,-,÷,×):")
l= input ("Enter Second number:")
f=float(f)
l=float(l)
print ("\n")
if (op=='+'):
	print (f,"+",l,"=",f+l)
if (op=='-'):
		print (f,"-",l,"=",f-l)
if (op=='÷'):
		print (f,"÷",l,"=",f/l)	
if (op=='×'):
	print (f,"×",l,"=",f*l)		